package Day04.Variables;

public class StaticVariableFund {
//-------------------------------------------------------------------------------------------------------------
    private int rollNo;
    private static String clgName;

//-------------------------------------------------------------------------------------------------------------

    static {
        System.out.println("----------MIT-------");
        clgName = "MIT" ;
    }

//-------------------------------------------------------------------------------------------------------------

    public static void displayClg(){
        System.out.println(clgName);
    }

//-------------------------------------------------------------------------------------------------------------

    public StaticVariableFund (){
        System.out.println("------------Default Constructor--------------");
    }

//-------------------------------------------------------------------------------------------------------------

    public StaticVariableFund (int rollNo ){
        System.out.println("------------Parameter Constructor--------------");
        this.rollNo = rollNo;
    }

// -------------------------------------------------------------------------------------------------------------

    public void displatInfo(){
        System.out.println("Details:" + rollNo);
    }

//-------------------------------------------------------------------------------------------------------------
}
